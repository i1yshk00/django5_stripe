"""Вспомогательные функции для отображения данных в кастомной админке.

Этот модуль специально держит небольшие presentation-level helpers отдельно от
`ModelAdmin`-классов. Благодаря этому:
- логика форматирования не размазывается по нескольким файлам;
- `list_display` и `readonly_fields` остаются короче и читаемее;
- одинаковое представление денег используется во всем backoffice единообразно.
"""


def format_money(value, currency: str) -> str:
    """Форматирует денежное значение для интерфейса админки.

    Args:
        value: Денежная сумма, как правило `Decimal`, уже рассчитанная моделью.
        currency: Валюта суммы в lowercase-формате (`usd`, `eur`).

    Returns:
        Строку вида `123.45 USD`, подходящую для list_display и readonly-полей.
    """
    # Внутри моделей валюта хранится в lowercase, потому что именно такой формат
    # ожидают Django choices и Stripe API. В интерфейсе админки, наоборот,
    # привычнее видеть человекочитаемый верхний регистр.
    return f'{value} {currency.upper()}'
