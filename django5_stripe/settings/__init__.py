"""Пакет настроек проекта.

Модуль автоматически выбирает dev- или prod-конфигурацию по переменной
окружения `DJANGO_ENV`, а затем дополнительно подключает настройки Stripe,
которые используются в обоих окружениях одинаково.
"""

import os

from .utils import load_project_dotenv


# Локальный `.env` подгружаем как можно раньше, еще до чтения `DJANGO_ENV`.
#
# Это делает поведение одинаковым для двух сценариев:
# 1. `docker compose`, где переменные чаще всего уже переданы извне;
# 2. `poetry run python manage.py ...`, где разработчик обычно ожидает, что
#    значения возьмутся из `.env` автоматически.
#
# Уже существующие переменные окружения не перетираются, поэтому внешний env
# по-прежнему имеет приоритет над файлом.
load_project_dotenv()


# Текущее окружение проекта.
# Допускаются значения dev / prod / production, все остальные трактуем как dev.
DJANGO_ENV = os.getenv('DJANGO_ENV', 'dev').lower()

if DJANGO_ENV in {'prod', 'production'}:
    from .prod import *
else:
    from .dev import *

# Stripe-конфигурация импортируется поверх выбранного окружения, чтобы
# переменные `STRIPE_*` и `DOMAIN_URL` были доступны как обычные Django settings.
from .stripe import *

# Конфигурация кастомной admin-панели на базе Unfold также подключается на
# верхнем уровне, чтобы словарь `UNFOLD` был доступен Django при старте проекта.
from .unfold import *
