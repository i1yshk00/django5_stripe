"""Настройки Stripe, общие для всех окружений проекта.

В этом модуле мы поддерживаем сразу два уровня конфигурации ключей:
1. базовый `STRIPE_SECRET_KEY` / `STRIPE_PUBLISHABLE_KEY`;
2. валютные keypair для `USD` и `EUR`.

Такой подход нужен по двум причинам:
- бонусный пункт ТЗ просит маршрутизацию по валюте;
- при этом в текущем проекте допустим и более простой режим, когда один
  Stripe account обслуживает обе валюты и обе валюты используют одну и ту же
  пару ключей.

Именно поэтому валютные переменные ниже работают с fallback на базовые ключи:
- если отдельные ключи для `USD` / `EUR` заданы, приложение использует их;
- если не заданы, проект прозрачно возвращается к общему keypair.
"""

import os


# Настройки Stripe вынесены в отдельный модуль, чтобы платежная конфигурация
# не смешивалась с общими Django-настройками.
#
# В этом проекте переменные ниже используются в двух местах:
# 1. На бэкенде, когда Django создает Checkout Session через Stripe SDK.
# 2. На фронтенде, когда страница товара инициализирует Stripe.js и делает
#    редирект пользователя на hosted Checkout страницу Stripe.

# Секретный API-ключ для серверных запросов в Stripe.
#
# Зачем нужен:
# - Django использует этот ключ для аутентификации запросов к Stripe API.
# - Без него бэкенд не сможет создавать Checkout Session, получать платежные
#   объекты и обрабатывать webhook-события.
#
# Как используется:
# - Передается в Stripe Python SDK / async client на сервере.
# - Никогда не должен попадать в шаблоны, JavaScript и ответы браузеру.
# - В локальной разработке обычно имеет вид "sk_test_...".
# - В production должен приходить из переменных окружения, а не из git.
STRIPE_SECRET_KEY = os.getenv('STRIPE_SECRET_KEY', '')

# Публичный ключ для клиентской инициализации Stripe.js.
#
# Зачем нужен:
# - Браузеру нужен этот ключ, чтобы создать экземпляр Stripe.js на странице
#   товара.
# - Он позволяет фронтенду вызвать redirectToCheckout(sessionId=...).
#
# Как используется:
# - Подставляется в HTML-страницу, которую рендерит Django.
# - В отличие от STRIPE_SECRET_KEY, этот ключ можно отдавать в браузер.
# - В локальной разработке обычно имеет вид "pk_test_...".
STRIPE_PUBLISHABLE_KEY = os.getenv('STRIPE_PUBLISHABLE_KEY', '')

# Валютно-специфичные ключи для USD.
#
# Зачем нужны:
# - позволяют использовать отдельную Stripe keypair для USD-потока;
# - закрывают бонусный пункт ТЗ про разные keypair на разные валюты.
#
# Почему сами переменные здесь не получают fallback сразу:
# - fallback должен вычисляться динамически в service-слое;
# - это позволяет корректно работать `override_settings` в тестах и не
#   "запекать" старое значение общего ключа в момент импорта настроек.
STRIPE_USD_SECRET_KEY = os.getenv('STRIPE_USD_SECRET_KEY', '')
STRIPE_USD_PUBLISHABLE_KEY = os.getenv(
    'STRIPE_USD_PUBLISHABLE_KEY',
    '',
)

# Валютно-специфичные ключи для EUR.
#
# Логика такая же, как у USD: если отдельные ключи не заданы, приложение
# использует базовый keypair текущего Stripe account.
STRIPE_EUR_SECRET_KEY = os.getenv('STRIPE_EUR_SECRET_KEY', '')
STRIPE_EUR_PUBLISHABLE_KEY = os.getenv(
    'STRIPE_EUR_PUBLISHABLE_KEY',
    '',
)

# Секрет подписи webhook endpoint-а Stripe.
#
# Зачем нужен:
# - Через этот секрет сервер проверяет, что входящее webhook-событие
#   действительно пришло от Stripe, а не от произвольного внешнего клиента.
# - Без него нельзя безопасно менять статусы заказов на основании внешних
#   HTTP-запросов.
#
# Как используется:
# - Передается в `stripe.Webhook.construct_event(...)` вместе с телом запроса
#   и заголовком `Stripe-Signature`.
# - В локальной разработке это значение обычно выдает команда
#   `stripe listen --forward-to ...`.
# - В production секрет берется из настроек конкретного webhook endpoint-а
#   в Stripe Dashboard.
STRIPE_WEBHOOK_SECRET = os.getenv('STRIPE_WEBHOOK_SECRET', '')

# Явно зафиксированная версия Stripe API, которую использует бэкенд.
#
# Зачем нужна:
# - Поведение Stripe может меняться между версиями API.
# - Фиксация версии делает запросы предсказуемыми во всех окружениях и
#   защищает от случайных изменений после апдейтов на стороне аккаунта Stripe.
#
# Дефолта в коде нет сознательно: значение должно приходить из окружения,
# чтобы CI, dev и production всегда отражали именно ту версию, под которую
# писался и тестировался код. Service-слой при пустом значении поднимает
# `ImproperlyConfigured` — лучше упасть на старте, чем тихо использовать
# неожиданную версию Stripe API.
STRIPE_API_VERSION = os.getenv('STRIPE_API_VERSION', '')

# Публичный базовый URL текущего приложения.
#
# Зачем нужен:
# - Stripe Checkout требует абсолютные значения success_url и cancel_url.
# - Бэкенд не сможет собрать корректные URL для редиректа, если не знает
#   внешний адрес приложения.
#
# Как используется:
# - Сервис checkout будет собирать URL вида:
#   "{DOMAIN_URL}/success" и "{DOMAIN_URL}/cancel".
# - В локальной разработке это обычно "http://localhost:8000".
# - На деплое здесь должен быть реальный HTTPS-домен приложения.
# - rstrip('/') убирает завершающий слеш, чтобы дальше не получить двойные
#   слеши при склейке путей.
DOMAIN_URL = os.getenv('DOMAIN_URL', 'http://localhost:8000').rstrip('/')

# Карта валют к соответствующим Stripe keypair.
#
# Это основной конфигурационный объект для service-слоя:
# - `api/services/stripe_client.py` берет отсюда ключи по `item.currency` или
#   `order.currency`;
# - views и доменные модели не знают деталей про переменные окружения;
# - на этапе `multi-currency` мы замыкаем все решение в одном месте.
STRIPE_CURRENCY_KEYPAIRS = {
    'usd': {
        'secret_key': STRIPE_USD_SECRET_KEY,
        'publishable_key': STRIPE_USD_PUBLISHABLE_KEY,
    },
    'eur': {
        'secret_key': STRIPE_EUR_SECRET_KEY,
        'publishable_key': STRIPE_EUR_PUBLISHABLE_KEY,
    },
}
