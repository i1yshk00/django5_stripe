#!/usr/bin/env sh

# Entry point контейнера Django-приложения.
#
# Его задача — сделать старт контейнера воспроизводимым:
# 1. при необходимости применить миграции;
# 2. при необходимости собрать static-файлы;
# 3. передать управление основному процессу контейнера.
#
# Такой подход важен для production-ready проекта, потому что уменьшает число
# ручных шагов при первом деплое и при последующих перезапусках.

set -eu


# По умолчанию контейнер сам применяет миграции.
# Это удобно и для production-like Docker Compose, и для локальной разработки.
if [ "${DJANGO_RUN_MIGRATIONS_ON_STARTUP:-true}" = "true" ]; then
    echo "Применяем миграции Django..."
    python manage.py migrate --noinput
fi


# Сборка static нужна прежде всего для production, но флаг оставлен настраиваемым,
# чтобы в dev-окружении можно было отключить лишнюю работу на старте.
if [ "${DJANGO_COLLECTSTATIC_ON_STARTUP:-true}" = "true" ]; then
    echo "Собираем static-файлы..."
    python manage.py collectstatic --noinput
fi


# После всех подготовительных шагов контейнер должен передать управление
# финальному процессу, чтобы корректно работали сигналы остановки и restart policy.
exec "$@"
